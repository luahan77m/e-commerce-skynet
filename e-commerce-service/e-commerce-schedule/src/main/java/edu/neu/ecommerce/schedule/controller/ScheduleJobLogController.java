package edu.neu.ecommerce.schedule.controller;

import edu.neu.ecommerce.schedule.entity.ScheduleJobLogEntity;
import edu.neu.ecommerce.schedule.service.ScheduleJobLogService;
import edu.neu.ecommerce.utils.PageUtils;
import edu.neu.ecommerce.utils.R;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

import java.util.Map;

/**
 * 定时任务日志
 *
 * @author Mark sunlightcs@gmail.com
 */
@RestController
@RequestMapping("/sys/scheduleLog")
public class ScheduleJobLogController {
	private final ScheduleJobLogService scheduleJobLogService;

	public ScheduleJobLogController(ScheduleJobLogService scheduleJobLogService) {
		this.scheduleJobLogService = scheduleJobLogService;
	}

	/**
	 * 定时任务日志列表
	 */
	@RequestMapping("/list")
	public R list(@RequestParam Map<String, Object> params){
		PageUtils page = scheduleJobLogService.queryPage(params);
		
		return R.ok().put("page", page);
	}
	
	/**
	 * 定时任务日志信息
	 */
	@RequestMapping("/info/{logId}")
	public R info(@PathVariable("logId") Long logId){
		ScheduleJobLogEntity log = scheduleJobLogService.getById(logId);
		
		return R.ok().put("log", log);
	}
}
